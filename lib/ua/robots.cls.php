	{
		foreach ($this->robots_lib as $id => $name) {
			$pat = "'".$id."'si";
			// if (preg_match($pat,$ua,$regs)) return array($id,$name);
			if (preg_match($pat,$ua,$regs)) {
				return array($id,$name);
			}
		}
		return array('','');
	}

